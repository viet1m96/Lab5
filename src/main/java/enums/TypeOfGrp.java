package enums;

/**
 * The {@code TypeOfGrp} enum represents the different types of groups or categories used for grouping or classifying data.
 *  Each constant represents a specific property or attribute.
 */
public enum TypeOfGrp {

    /**
     * Represents the ID type.  Used for grouping by identifier.
     */
    ID,

    /**
     * Represents the String type. Used for grouping by string values.
     */
    STRING,

    /**
     * Represents the X-coordinate type. Used for grouping by X-coordinate values.
     */
    X,

    /**
     * Represents the Y-coordinate type. Used for grouping by Y-coordinate values.
     */
    Y,

    /**
     * Represents the Count type. Used for grouping by a count or quantity.
     */
    COUNT,

    /**
     * Represents the Education form type. Used for grouping by the form of education.
     */
    EDU,

    /**
     * Represents the Semester type. Used for grouping by semester.
     */
    SEMESTER
}