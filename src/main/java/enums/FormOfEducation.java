package enums;

/**
 * The {@code FormOfEducation} enum represents the different forms of education available.
 * It provides a type-safe way to represent and work with these different forms of education within the application.
 */
public enum FormOfEducation {

    /**
     * Represents distance education, where students learn remotely.
     */
    DISTANCE_EDUCATION,

    /**
     * Represents full-time education, where students attend classes regularly during the day.
     */
    FULL_TIME_EDUCATION,

    /**
     * Represents evening classes, which are typically held in the evening for students who work during the day.
     */
    EVENING_CLASSES
}